// Array.push is actually used to add the items in the last of the Array 
const MyArray = ['One' ,'Two' ,'three','Four','Five']
MyArray.push('six','seven')
console.log(MyArray)




//Array.unshift is actually used to add the items in the starting of the Array
const MyArray2 = ['three','four','five','six']
MyArray2.unshift('one','two')
console.log(MyArray2)



//Array.pop is actually used to remove the items from last of the Array
const MyArray3 = ['one','two','three','four','five','Six','seven','Eight','nine']
MyArray3.pop()
MyArray3.pop()
MyArray3.pop()
console.log(MyArray3)


//Array.shift is actually used to remove the items from the starting of the Array
const MyArray4 = ['one','two','three','four','five','six','seven']
MyArray4.shift()
MyArray4.shift()
MyArray4.shift()
console.log(MyArray4)

